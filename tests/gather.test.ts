import rewire from "rewire"































































































































































































































































const gather = rewire("../src/gather")































































































































































































































































const getGuestClub = gather.__get__("getGuestClub")































































































































































































































































// @ponicode
describe("getGuestClub", () => {
    test("0", () => {
        let result: any = getGuestClub("SV Phoenix Jongens B3")
        expect(result).toEqual({ guestClub: "Phoenix", guestTeam: "JB3" })
    })

    test("1", () => {
        let result: any = getGuestClub("SV Phoenix Meisjes C3")
        expect(result).toEqual({ guestClub: "Phoenix", guestTeam: "MC3" })
    })
})
